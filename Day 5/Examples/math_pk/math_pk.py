"""
This is Mathematical Operations User Defined Package with ADDITION, SUBTRACTION, MULTIPLICATION, DIVISION
"""


def addition(a, b):
    """
    This is return addition of two variables
    """
    return a + b


def subtraction(a, b):
    """
    This is return subtraction of two variables
    """
    return a - b


def multiplication(a, b):
    """
    This is return multiplication of two variables
    """
    return a * b


def division(a, b):
    """
    This is return division of two variables
    """
    return a / b


